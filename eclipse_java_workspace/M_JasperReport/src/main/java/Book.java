import java.math.BigDecimal;

public class Book {
	private String title;
	private BigDecimal id;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public BigDecimal getId() {
		return id;
	}
	public void setId(BigDecimal id) {
		this.id = id;
	}
 
	
}
