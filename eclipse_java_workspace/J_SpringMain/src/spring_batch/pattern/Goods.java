package spring_batch.pattern;

public class Goods
{
    private String fruite;
    private int price;

    public String getFruite()
    {
        return fruite;
    }

    public void setFruite(String fruite)
    {
        this.fruite = fruite;
    }

    public int getPrice()
    {
        return price;
    }

    public void setPrice(int price)
    {
        this.price = price;
    }

}
